package warOfFlowers;

public class Sound {

	String title;
	String soundPath;
	
	public Sound(String title, String path) {
		super();
		this.title = title;
		this.soundPath = path;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getSoundPath() {
		return soundPath;
	}

	public void setSoundPath(String path) {
		this.soundPath = path;
	}
	
	
	
	
}
